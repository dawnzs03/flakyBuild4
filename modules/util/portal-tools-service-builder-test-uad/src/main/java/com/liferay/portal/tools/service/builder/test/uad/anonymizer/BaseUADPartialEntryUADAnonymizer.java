package com.liferay.portal.tools.service.builder.test.uad.anonymizer;

import com.liferay.asset.kernel.model.AssetEntry;
import com.liferay.asset.kernel.service.AssetEntryLocalService;
import com.liferay.portal.kernel.dao.orm.ActionableDynamicQuery;
import com.liferay.portal.kernel.exception.PortalException;
import com.liferay.portal.kernel.model.User;
import com.liferay.portal.tools.service.builder.test.model.UADPartialEntry;
import com.liferay.portal.tools.service.builder.test.service.UADPartialEntryLocalService;
import com.liferay.portal.tools.service.builder.test.uad.constants.SBTestUADConstants;
import com.liferay.user.associated.data.anonymizer.DynamicQueryUADAnonymizer;

import org.osgi.service.component.annotations.Reference;

/**
 * Provides the base implementation for the uad partial entry UAD anonymizer.
 *
 * <p>
 * This implementation exists only as a container for the default methods
 * generated by ServiceBuilder. All custom service methods should be put in
 * {@link UADPartialEntryUADAnonymizer}.
 * </p>
 *
 * @author Brian Wing Shun Chan
 * @generated
 */
public abstract class BaseUADPartialEntryUADAnonymizer
	extends DynamicQueryUADAnonymizer<UADPartialEntry> {

	@Override
	public void autoAnonymize(
			UADPartialEntry uadPartialEntry, long userId, User anonymousUser)
		throws PortalException {

		if (uadPartialEntry.getUserId() == userId) {
			uadPartialEntry.setUserId(anonymousUser.getUserId());
			uadPartialEntry.setUserName(anonymousUser.getFullName());

			autoAnonymizeAssetEntry(uadPartialEntry, anonymousUser);
		}

		uadPartialEntryLocalService.updateUADPartialEntry(uadPartialEntry);
	}

	@Override
	public void delete(UADPartialEntry uadPartialEntry) throws PortalException {
		uadPartialEntryLocalService.deleteUADPartialEntry(uadPartialEntry);
	}

	@Override
	public Class<UADPartialEntry> getTypeClass() {
		return UADPartialEntry.class;
	}

	protected void autoAnonymizeAssetEntry(
		UADPartialEntry uadPartialEntry, User anonymousUser) {

		AssetEntry assetEntry = fetchAssetEntry(uadPartialEntry);

		if (assetEntry != null) {
			assetEntry.setUserId(anonymousUser.getUserId());
			assetEntry.setUserName(anonymousUser.getFullName());

			assetEntryLocalService.updateAssetEntry(assetEntry);
		}
	}

	@Override
	protected ActionableDynamicQuery doGetActionableDynamicQuery() {
		return uadPartialEntryLocalService.getActionableDynamicQuery();
	}

	@Override
	protected String[] doGetUserIdFieldNames() {
		return SBTestUADConstants.USER_ID_FIELD_NAMES_UAD_PARTIAL_ENTRY;
	}

	protected AssetEntry fetchAssetEntry(UADPartialEntry uadPartialEntry) {
		return assetEntryLocalService.fetchEntry(
			UADPartialEntry.class.getName(),
			uadPartialEntry.getUadPartialEntryId());
	}

	@Reference
	protected AssetEntryLocalService assetEntryLocalService;

	@Reference
	protected UADPartialEntryLocalService uadPartialEntryLocalService;

}