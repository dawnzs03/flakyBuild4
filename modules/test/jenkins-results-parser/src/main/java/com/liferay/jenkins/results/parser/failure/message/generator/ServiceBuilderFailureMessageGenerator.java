package com.liferay.jenkins.results.parser.failure.message.generator;

import com.liferay.jenkins.results.parser.Build;

import org.dom4j.Element;

/**
 * @author Brittney Nguyen
 */
public class ServiceBuilderFailureMessageGenerator
	extends BaseFailureMessageGenerator {

	@Override
	public Element getMessageElement(Build build) {
		String consoleText = build.getConsoleText();

		if (!consoleText.contains(_TOKEN_DETECTED_BUILD_SERVICE_CHANGES)) {
			return null;
		}

		int start = consoleText.indexOf(_TOKEN_GIT_DIFF_VERSION);

		start = consoleText.lastIndexOf("\n", start);

		int end = start + CHARS_CONSOLE_TEXT_SNIPPET_SIZE_MAX;

		end = consoleText.lastIndexOf("\n", end);

		return getConsoleTextSnippetElement(consoleText, false, start, end);
	}

	private static final String _TOKEN_DETECTED_BUILD_SERVICE_CHANGES =
		"Detected build services changes";

	private static final String _TOKEN_GIT_DIFF_VERSION = "--- a";

}